    private void popuniGradove() {
        try {
            ArrayList<Grad> gradovi = ClientController.getInstance().getAllGrad();

            cmbGrad.removeAllItems();

            for (Grad grad : gradovi) {
                cmbGrad.addItem(grad);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviTim.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniTimove() {

        try {
            ArrayList<Tim> timovi = ClientController.getInstance().getAllTim();

            cmbCrveniTim.removeAllItems();
            cmbPlaviTim.removeAllItems();

            for (Tim tim : timovi) {
                cmbCrveniTim.addItem(tim);
                cmbPlaviTim.addItem(tim);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviTim.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void popuniIgrace() {

        try {

            crveniTim = (Tim) cmbCrveniTim.getSelectedItem();
            plaviTim = (Tim) cmbPlaviTim.getSelectedItem();

            crveniIgraci = ClientController.getInstance().getAllIgrac(crveniTim);
            plaviIgraci = ClientController.getInstance().getAllIgrac(plaviTim);

            cmbIgrac.removeAllItems();

            for (Igrac igrac : crveniIgraci) {
                cmbIgrac.addItem(igrac);
            }

            for (Igrac igrac : plaviIgraci) {
                cmbIgrac.addItem(igrac);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

