    private void btnDodajMecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajMecActionPerformed

        try {

            Grad grad = (Grad) cmbGrad.getSelectedItem();
            int rundeCrveni = Integer.parseInt(txtRundeCrveni.getText());
            int rundePlavi = Integer.parseInt(txtRundePlavi.getText());
            String MVP = "";

            TableModelStatistika tmStat = (TableModelStatistika) tblStatistika.getModel();

            if (tmStat.getLista().size() != 6) {
                JOptionPane.showMessageDialog(this, "Niste uneli statistiku za svakog igraca!");
                return;
            }
            
            if(!tmStat.postojiMVP()){
                JOptionPane.showMessageDialog(this, "Jedan igrac mora da bude MVP!");
                return;
            }

            for (StatistikaIgraca si : tmStat.getLista()) {
                if (si.isMVP()) {
                    MVP = si.getIgrac().getNickname();
                }
            }

            Mec m = new Mec(null, rundeCrveni, rundePlavi, MVP,
                    crveniTim, plaviTim, grad, a, tmStat.getLista());

            ClientController.getInstance().addMec(m);
            
            tmStat.clear();
            
            TableModelMecevi tmMec = (TableModelMecevi) tblMecevi.getModel();
            tmMec.refreshTable();
            
            JOptionPane.showMessageDialog(this, "Uspesno dodat mec.");

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnDodajMecActionPerformed

